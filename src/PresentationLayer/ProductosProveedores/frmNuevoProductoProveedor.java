    private void btnOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOkActionPerformed

        if (idProductoProveedor == 0) {
            if (new ProductosProveedores(0,
                    idProveedor.get(cbProveedor.getSelectedIndex()),
                    idProducto.get(cbProducto.getSelectedIndex())).Add() < 0) {
                JOptionPane.showMessageDialog(null, "El Registro ya exite!");
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Registro exitoso!");
                this.dispose();
            }
        } else {
            productoproveedor.setIdProducto(idProducto.get(cbProducto.getSelectedIndex()));
            productoproveedor.setIdProveedor(idProveedor.get(cbProveedor.getSelectedIndex()));
            if (productoproveedor.Update() < 0) {
                JOptionPane.showMessageDialog(null, "El Registro ya exite!");
                this.dispose();

            } else {
                JOptionPane.showMessageDialog(null, "Registro actualizado correctamente!");
                this.dispose();
            }

        }
    }//GEN-LAST:event_btnOkActionPerformed

