    public void llenarProductos() {
        DefaultTableModel res = producto.GetAllModel();
        for (int i = 0; i < res.getRowCount(); i++) {
            cbProducto.addItem(String.valueOf(res.getValueAt(i, 1)));
            idProducto.add(Integer.parseInt(String.valueOf(res.getValueAt(i, 0))));
        }
    }

    public void llenarProveedores() {
        DefaultTableModel res = proveedor.GetAllModel();
        for (int i = 0; i < res.getRowCount(); i++) {
            cbProveedor.addItem(String.valueOf(res.getValueAt(i, 1)));
            idProveedor.add(Integer.parseInt(String.valueOf(res.getValueAt(i, 0))));
        }
    }

